n1 = int(input('Enter the first number: '))
n2 = int(input('Enter the second number: '))
print ('Operators: +, -, *, /')
o1 = input('Enter the operator: ')
if o1 == '+':
    print (n1 + n2)
elif o1 == '-':
    print (n1 - n2)
elif o1 == '*':
    print (n1 * n2)
elif o1 == '/':
    print (n1 / n2)
